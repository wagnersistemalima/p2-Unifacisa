package entities;

public class DataAniversario {
	
	// atributos basicos
	
	private int dia;
	private int mes;
	
	// construtor
	
	
	public DataAniversario(int dia, int mes) {
		this.dia = dia;
		this.mes = mes;
	}
	
	// Getters & setters


	public int getDia() {
		return dia;
	}


	public void setDia(int dia) {
		this.dia = dia;
	}


	public int getMes() {
		return mes;
	}


	public void setMes(int mes) {
		this.mes = mes;
	}
	
	

}
